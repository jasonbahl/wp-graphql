// Supported pseudo classes.
export const pseudoClasses = [
    null,
    "active",
    "checked",
    "disabled",
    "focus",
    "hover",
    "visited",
]

export const allProps = {
    ...cssProps,
    ...aliasProps,
    variant: ["variant"],
}

export const styleProps = {}

Object.keys(allProps).forEach((propName) =>
    pseudoClasses.forEach(
        (pseudoClass) =>
            (styleProps[
                pseudoClass === null
                    ? propName
                    : `${pseudoClass}${
                    propName.charAt(0).toUpperCase() + propName.slice(1)
                        }`
                ] = {
                pseudoClass,
                names: allProps[propName],
            })
    )
)
